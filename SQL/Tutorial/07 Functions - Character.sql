CHR:          returns the CHARACTER equivalent OF the NUMBER it uses AS an argument
CONCAT:     splices two strings together
INITCAP:    capitalizes the FIRST letter OF a word AND makes ALL other characters lowercase.
LOWER:         changes ALL the characters TO LOWER CASE
UPPER:      changes ALL the characters TO UPPER CASE
LPAD:         pads the LEFT side OF a string WITH a specified CHARACTER
RPAD:          pads the RIGHT side OF a string WITH a specified CHARACTER
LTRIM:      removes ALL spaces ON the LEFT side OF a string
RTRIM:      removes ALL spaces ON the RIGHT side OF a string
REPLACE:      replaces ALL occurences OF a string WITH a specified string
SUBSTR:      returns a specified LENGTH FROM within a given string
TRANSLATE"     s three arguments: the target string, the FROM string, and the TO string. Elements of the target string that occur in the FROM string are translated to the corresponding element in the TO string
INSTR:         determines WHERE a string IS FOUND within another
LENGTH:     returns the LENGTH OF its lone CHARACTER argument


