ABS:      returns the ABSOLUTE VALUE OF a NUMBER
CEIL:     returns the smallest INTEGER greater than OR equal TO its argument
FLOOR:  returns the largest INTEGER equal TO OR less than its argument
COS, COSH, SIN, SINH, TAN, AND TANH: returns the appropriate trigonometric VALUE. argument IS IN radians
EXP:      raise e TO a power
LN:      returns the logarithm TO base e OF the argument
LOG:      returns the logarithm TO base 10 OF the argument
MOD:     returns remainder OF a division
POWER:     raise one NUMBER TO the power OF another
SIGN:      returns -1 if its argument IS less than 0, 0 if its argument IS equal TO 0, AND 1 if its argument IS greater than 0
SQRT:      returns the square root OF an argument (cannot use SQRT ON negative numbers)

