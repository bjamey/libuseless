the || (DOUBLE pipe) concatenates two string:
note that SOME applications use the plus (+) sign TO concatenate strings:
SELECT firstname || lastname (EntireName), phone  FROM  customers
OR
SELECT firstname  ||  ","  ||  lastname (EntireName), phone  FROM  customers


